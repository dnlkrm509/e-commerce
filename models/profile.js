export class Profile {
    constructor(imageUri, fullName, dateOfBirth, gender, email, phoneNumber, id) {
        this.imageUri = imageUri;
        this.fullName = fullName;
        this.dateOfBirth = dateOfBirth;
        this.gender = gender;
        this.email = email;
        this.phoneNumber = phoneNumber;
        this.id = id;
    }
}